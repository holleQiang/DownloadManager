package com.zhangqiang.downloadmanager.plugin.m3u8.parser.bean;

public class Resolution {
    private int width;
    private int height;

    public int getWidth() {
        return width;
    }

    public Resolution setWidth(int width) {
        this.width = width;
        return this;
    }

    public int getHeight() {
        return height;
    }

    public Resolution setHeight(int height) {
        this.height = height;
        return this;
    }
}
